package com.mycompany.relatorio;

public class Relatorio {

   private String dataRelatorio;
   private String horaRelario;
   private String funcionarioRelatorio;
   private String nivelDeMaturacaoRelatorio;

    public String getDataRelatorio() {
        return dataRelatorio;
    }
    public void setDataRelatorio(String dataRelatorio) {
        this.dataRelatorio = dataRelatorio;
    }
    public String getHoraRelario() {
        return horaRelario;
    }
    public void setHoraRelario(String horaRelario) {
        this.horaRelario = horaRelario;
    }
    public String getFuncionarioRelatorio() {
        return funcionarioRelatorio;
    }
    public void setFuncionarioRelatorio(String funcionarioRelatorio) {
        this.funcionarioRelatorio = funcionarioRelatorio;
    }
    public String getNivelDeMaturacaoRelatorio() {
        return nivelDeMaturacaoRelatorio;
    }
    public void setNivelDeMaturacaoRelatorio(String nivelDeMaturacaoRelatorio) {
        this.nivelDeMaturacaoRelatorio = nivelDeMaturacaoRelatorio;
    }

}
